    private void bValoresCorrectosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bValoresCorrectosActionPerformed
        // TODO add your handling code here:
        int indiceSeleccionado = jListaProductos.getSelectedIndex();
        switch (indiceSeleccionado){
            case 1:
            case 2:
            case 3:
                productoCorrecto=true;
                break;
            default:
                productoCorrecto=false;
                
        }
        if((fabricanteCorrecto)&&(productoCorrecto)){
            JOptionPane.showMessageDialog(this, "Valores correctos.");
        }else{
            JOptionPane.showMessageDialog(this, "Valores erroneos.");    
        }
    }//GEN-LAST:event_bValoresCorrectosActionPerformed

