    private void jListaFabricantesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jListaFabricantesActionPerformed
        // TODO add your handling code here:
        int indiceSeleccionado = jListaFabricantes.getSelectedIndex();
        switch (indiceSeleccionado){
            case 1:
                jListaProductos.removeAllItems();
                jListaProductos.addItem("Seleccione un modelo:");
                jListaProductos.addItem("RX Series 500");
                jListaProductos.addItem("Vega Series");
                jListaProductos.addItem("RX Series 6000");
                fabricanteCorrecto=true;
                break;
            case 2:
                jListaProductos.removeAllItems();
                jListaProductos.addItem("Seleccione un modelo:");
                jListaProductos.addItem("HD 3000");
                jListaProductos.addItem("HD 4000");
                jListaProductos.addItem("IRIS 600");
                fabricanteCorrecto=true;
                break;                
            case 3:
                jListaProductos.removeAllItems();
                jListaProductos.addItem("Seleccione un modelo:");
                jListaProductos.addItem("GTX Serie 1000");
                jListaProductos.addItem("GTX Serie 2000");
                jListaProductos.addItem("GTX Serie 3000");
                fabricanteCorrecto=true;
                break;
            default:
                jListaProductos.removeAllItems();
                fabricanteCorrecto=false;
                
               
        }
    }//GEN-LAST:event_jListaFabricantesActionPerformed

